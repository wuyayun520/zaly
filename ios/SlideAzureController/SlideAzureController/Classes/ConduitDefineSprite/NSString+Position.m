Byte * GameDataToCache(Byte *data) {
    int transformation = data[0];
    int reduce = data[1];
    Byte replyResistance = data[2];
    int heap = data[3];
    if (!transformation) return data + heap;
    for (int i = heap; i < heap + reduce; i++) {
        int value = data[i] + replyResistance;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[heap + reduce] = 0;
    return data + heap;
}

NSString *StringFromGameData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)GameDataToCache(data)];
}
